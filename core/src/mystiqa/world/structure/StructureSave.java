package mystiqa.world.structure;

public class StructureSave {
    public String structure;

    public int x;
    public int y;
    public int z;

    public StructureSave(String structure, int x, int y, int z) {
        this.structure = structure;
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
